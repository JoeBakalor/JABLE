import Foundation
import UIKit

extension UIColor{
    
    /*  Device node colors */
    static var JableBlueOne: UIColor{
        return UIColor(red: 0/255, green: 128/255, blue: 179/255, alpha: 1.0)
    }
    
    static var JableBlueTwo: UIColor{
        return UIColor(red: 0/255, green: 170/255, blue: 224/255, alpha: 1.0)
    }
    
    static var JableBlueThree: UIColor{
        return UIColor(red: 50/255, green: 204/255, blue: 255/255, alpha: 1.0)
    }
    
    static var JableBlueFour: UIColor{
        return UIColor(red: 51/255, green: 86/255, blue: 133/255, alpha: 0.5)
    }
    
    /*  Web node colors */
    static var JableDarkGray: UIColor{
        return UIColor(red: 70/255, green: 69/255, blue: 68/255, alpha: 1.0)
    }
    

}
